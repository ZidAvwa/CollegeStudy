    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Product p = new Product();
        p.setId(Integer.parseInt(txtId.getText()));
        
        Category selectedCat = (Category) cmbCategory.getSelectedItem();
        p.setCategoryId(selectedCat.getIdCategory());
        
        p.setName(txtName.getText());
        p.setPrice(Double.parseDouble(txtPrice.getText()));
        p.setStock(Integer.parseInt(txtStock.getText()));
        p.setSpecs(txtSpecs.getText());
        
        p.save();
        
        txtId.setText(Integer.toString(p.getId()));
        tampilkanData();
        JOptionPane.showMessageDialog(null, "Data Saved Successfully!");
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        int row = tblProduct.getSelectedRow();
        
        if (row >= 0) {
            Product p = new Product();
            p.setId(Integer.parseInt(model.getValueAt(row, 0).toString()));
            p.delete();
            
            kosongkanForm();
            tampilkanData();
        } else {
            JOptionPane.showMessageDialog(null, "Select a row to delete!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        int row = tblProduct.getSelectedRow();
        
        Product p = new Product().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtId.setText(String.valueOf(p.getId()));
        txtName.setText(p.getName());
        txtPrice.setText(String.valueOf(p.getPrice()));
        txtStock.setText(String.valueOf(p.getStock()));
        txtSpecs.setText(p.getSpecs());
        
        for (int i = 0; i < cmbCategory.getItemCount(); i++) {
            Category cat = (Category) cmbCategory.getItemAt(i);
            if (cat.getIdCategory() == p.getCategoryId()) {
                cmbCategory.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_tblProductMouseClicked


    public void tampilkanData() {
        String[] columns = {"ID", "Category", "Name", "Price", "Stock", "Specs"};
        ArrayList<Product> list = new Product().getAll();
        Object[] row = new Object[6];
        
        DefaultTableModel model = new DefaultTableModel(null, columns);
        
        for (Product p : list) {
            row[0] = p.getId();
            row[1] = p.getCategoryName();
            row[2] = p.getName();
            row[3] = p.getPrice();
            row[4] = p.getStock();
            row[5] = p.getSpecs();
            model.addRow(row);
        }
        tblProduct.setModel(model);
    }

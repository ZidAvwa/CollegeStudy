    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Peminjaman pem = new Peminjaman();
        pem.setIdpeminjaman(Integer.parseInt(txtId.getText()));
        pem.getAnggota().setIdanggota(Integer.parseInt(txtIdAnggota.getText()));
        pem.getBuku().setIdbuku(Integer.parseInt(txtIdBuku.getText()));
        pem.setTanggalPinjam(txtTanggalPinjam.getText());
        pem.setTanggalKembali(txtTanggalKembali.getText());
        pem.save();

        txtId.setText(Integer.toString(pem.getIdpeminjaman()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

